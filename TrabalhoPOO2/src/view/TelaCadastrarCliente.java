    protected void inserirClienteDb(){
        JOptionPane.showMessageDialog(this, "A operação foi um sucesso.", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
        
        
        clienteController.inserir(getCadNome().getText(), getCadTelefone().getText(), getCadEndereco().getText(), getCadCNPJ().getText());
       
        DefaultTableModel model = (DefaultTableModel) cadTab.getModel();
        model.setRowCount(0);
        preencheTabela(); 
    }
    
    protected void preencheTabela(){
        
         List<ClienteModel> listCliente = new ArrayList<>();
         listCliente = clienteController.listar();
         DefaultTableModel model = (DefaultTableModel) cadTab.getModel();
         
         for(ClienteModel cliente : listCliente){
            model.addRow(new Object[]{cliente.getId(), cliente.getNome(), cliente.getCnpj(), cliente.getTelefone(), cliente.getEndereco()});    
         }
    }
    
    protected Boolean validarCNPJ(String CNPJ){
        if(CNPJ.strip().length() != 19) return false;
        else return true;
    }
    
    protected Boolean validarTel(String CNPJ){
        System.out.println();
        if(CNPJ.strip().length() != 16) return false;
        else return true;
    }

