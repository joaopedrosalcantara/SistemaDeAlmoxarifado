    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        // TODO add your handling code here:
        int linha = cadTab.getSelectedRow();
        int idDelet = (int) cadTab.getValueAt(linha, 0);
        
        clienteController.deletar(idDelet);
        DefaultTableModel model = (DefaultTableModel) cadTab.getModel();
        model.setRowCount(0);
        preencheTabela();         
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int linha = cadTab.getSelectedRow();
        int idEdit = (int) cadTab.getValueAt(linha, 0);
        TelaEditarCliente edit = new TelaEditarCliente(idEdit);
        edit.setVisible(true);
    }//GEN-LAST:event_btnEditarActionPerformed

