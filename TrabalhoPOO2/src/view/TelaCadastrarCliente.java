    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if(!validarCNPJ(getCadCNPJ().getText())){
            JOptionPane.showMessageDialog(this, "CNPJ inválido!", "Erro ao cadastrar", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!validarTel(getCadTelefone().getText())){
            JOptionPane.showMessageDialog(this, "Telefone inválido!", "Erro ao cadastrar", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(getCadEndereco().getText().length() == 0 || getCadNome().getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!", "Erro ao cadastrar", JOptionPane.ERROR_MESSAGE);
            return;
        }
        inserirClienteDb();
    }//GEN-LAST:event_btnCadastrarActionPerformed

